const _defaultTextStyle = TextStyle();

class GoogleFontStyleJsonConverter implements JsonConverter<TextStyle, String> {
  const GoogleFontStyleJsonConverter();

  @override
  TextStyle fromJson(String json) {
    try {
      final textStyle = parseCssFont(json);
      if (textStyle.fontFamily != null &&
          _googleFontsMap.containsKey(textStyle.fontFamily!.toLowerCase())) {
        return _googleFontsMap[textStyle.fontFamily!.toLowerCase()]!(
          background: textStyle.background,
          backgroundColor: textStyle.backgroundColor,
          color: textStyle.color,
          decoration: textStyle.decoration,
          decorationColor: textStyle.decorationColor,
          decorationStyle: textStyle.decorationStyle,
          decorationThickness: textStyle.decorationThickness,
          fontFeatures: textStyle.fontFeatures,
          fontSize: textStyle.fontSize,
          fontStyle: textStyle.fontStyle,
          fontWeight: textStyle.fontWeight,
          foreground: textStyle.foreground,
          height: textStyle.height,
          letterSpacing: textStyle.letterSpacing,
          locale: textStyle.locale,
          shadows: textStyle.shadows,
          textBaseline: textStyle.textBaseline,
          wordSpacing: textStyle.wordSpacing,
        );
      } else {
        return textStyle;
      }
    } catch (_) {
      return _defaultTextStyle;
    }
  }

  @override
  String toJson(TextStyle object) => object.toString();
}

